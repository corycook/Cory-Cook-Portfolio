<p><b>Should an earthquake strike while you are in a campus building, do the following:
</b><b></b></p>
<b></b><b></b>
<p><b>Duck </b>- duck or drop down on the floor. </p>
<p><b>Cover </b>- take cover under a sturdy desk, table or other furniture. If that 
is not possible, seek cover against an interior wall and protect your head and neck 
with your arms. Avoid danger spots near windows, hanging objects, mirrors or tall 
furniture. </p>
<p><b>Hold </b>- if you take cover under a sturdy piece of furniture, HOLD on to 
it and be prepared to move with it. Hold the position until the ground stops shaking 
and it is safe to move. </p>
<p></p>
<ul type="disc">
	<li>If you are in a hallway, drop to the floor against an interior wall. Protect 
	your head and neck with your arms. </li>
	<li>Do not enter or exit the building during the shaking - there is danger from 
	falling debris. </li>
	<li>Do not use the elevators. </li>
	<li>If you are outdoors, find a spot away from buildings, trees, streetlights 
	and power lines. Drop to the ground and stay there until the shaking stops. 
	Do not return to your building until authorized. </li>
	<li>In a car, stop in the safest place away from underpasses/overpasses, bridges, 
	etc. Stay in the vehicle until the shaking stops. </li>
	<li><b>BE PREPARED FOR AFTERSHOCKS! </b>
	<p></p>
	</li>
</ul>